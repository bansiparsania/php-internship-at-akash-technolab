<?php

//pre increment
$a=24;
echo "a=$a";
echo "<br>";
echo "pre-increment";
echo ++$a;
echo "<br>";

//post increment
echo "post-increment";
echo $a++;
echo "<br>";
echo "after post increment a=$a";

echo "<br>";

//pre decrement
$a=10;
echo "a=$a";
echo "<br>";
echo "pre-decrement";
echo --$a;
echo "<br>";

//post decrement
echo "post-decrement";
echo $a--;
echo "<br>";
echo "after post decrement a=$a";

?>